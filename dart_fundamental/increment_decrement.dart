// Increment and decrement in dart

void main() {
    var a = 0;
    var b = 5;
    
    a++; // increment in python its same like a += 1
    b--; // decrement, in python its same like a -= 1
    
    print(a);
    print(b);
    
    // Increment and while loops
    var c = 3;
    while (c < 7) {
        c++; 
        if (c == 7) {
            break;
        }
    print(c);
    }
    
}
