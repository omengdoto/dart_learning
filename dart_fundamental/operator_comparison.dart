// Operator comparison
void main() {
    // ==
    var a = 5;
    var b = 7;
    
    if (a == b)
    print('True'); 
    else
    print('False');
    
    // !=
    if (a != b)
    print('True');
    else
    print('False');
    
    // >
    if (a > b)
    print('True');
    else
    print('False');
    
    // <
    if (a < b)
    print('True');
    else
    print('False');
}
